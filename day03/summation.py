# 1.有一列分数序列，2／1，3／2，5／3，8／5。。。求出前20项之和
# 将分子分母分开设变量
a = 2
b = 1
# c用于记录变化前a的值，方便赋值给b
c = 0
# 记录每次相加的和
sum = 0
for i in range(1,21):# 循环20次，计算前20项之和
    sum += a/b
    # 找到分数之间的规律，分子等于前一位分子分母的和，分母等于前一项的分子，开始计算
    c = a # 先将a的值赋给c
    a = a+b #计算下一步a的值
    print("a:",a)
    b = c  # 将上一步a的值（c）赋给b
    print("b:",b)
#输出最后的结果
print("总和为:",sum)